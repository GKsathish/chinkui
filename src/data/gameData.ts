/**
 * Helper function to get games by category
 */
export const getGamesByCategory = (category: string): Table[] => {
  return GAME_DATA.filter(game => game.category === category);
};

/**
 * Helper function to get a specific game by tableId
 */
export const getGameById = (tableId: string): Table | undefined => {
  return GAME_DATA.find(game => game.tableId === tableId);
};

/**
 * Helper function to get a specific game by slug
 */
export const getGameBySlug = (slug: string): Table | undefined => {
  return GAME_DATA.find(game => game.slug === slug);
};
